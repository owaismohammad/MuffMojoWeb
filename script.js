// Function to open the modal and display the emoji and its name
function searchEmoji() {
    const emojiInput = document.getElementById('emoji-search').value;
    const emojiName = getEmojiName(emojiInput);

    if (emojiName) {
        openModal(emojiInput, emojiName); // Pass both emoji and name to openModal
    } else {
        openModal("😢", "Sorry! I was too dumb to not add it"); // Display the 'not found' emoji and message
    }
}

