// Function to open the modal
function openModal(emoji, emojiName) {
    const emojiElement = document.getElementById('emoji-element');
    const emojiNameElement = document.getElementById('emoji-name');

    // Set the emoji and its name inside the modal
    emojiElement.innerText = emoji; // Display the emoji
    emojiNameElement.innerText = emojiName; // Display the corresponding name

    document.getElementById('emoji-modal').style.display = 'flex'; // Show the modal
}

// Function to close the modal when clicked outside the modal content
function closeModalOnClick(event) {
    // Check if the click is on the background (not on modal content)
    if (event.target === document.getElementById('emoji-modal')) {
        closeModal(); // Close the modal if the background is clicked
    }
}

// Function to close the modal
function closeModal() {
    document.getElementById('emoji-modal').style.display = 'none'; // Hide the modal
}
